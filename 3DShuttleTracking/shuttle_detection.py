# ── Tuning knobs ──────────────────────────────────────────────────────────────
SMOOTH_SIGMA    = 1.2   # Gaussian σ on detected-frame positions
GAP_FILL_FRAMES = 5     # interpolate NaN gaps ≤ this many frames
TRAIL_LEN       = 20    # frames of amber trail drawn in debug video

# ── TrackNetV3 exact weight filenames (from the official repo) ────────────────
TRACKNET_PT   = "TrackNet_best.pt"     # capital T, capital N
INPAINTNET_PT = "InpaintNet_best.pt"   # capital I, capital N


# ─────────────────────────────────────────────────────────────────────────────
#  STEP 1 — VALIDATE WEIGHTS FOLDER
# ─────────────────────────────────────────────────────────────────────────────
def _validate_weights(weights_dir: Path):
    """
    Confirm predict.py and both .pt files are present.
    Raises FileNotFoundError with a helpful message if anything is missing.
    """
    missing = []
    for fname in ["predict.py", TRACKNET_PT, INPAINTNET_PT]:
        if not (weights_dir / fname).exists():
            missing.append(fname)

    if missing:
        raise FileNotFoundError(
            f"Missing files in {weights_dir}:\n"
            + "".join(f"  ✗  {m}\n" for m in missing)
            + "\nExpected layout:\n"
            "  tracknet_weights/\n"
            "    ├── predict.py\n"
            f"    ├── {TRACKNET_PT}\n"
            f"    └── {INPAINTNET_PT}\n"
            "\nDownload checkpoints from the TrackNetV3 Google Drive link\n"
            "in the README and unzip into your weights folder."
        )


# ─────────────────────────────────────────────────────────────────────────────
#  STEP 2 — RUN TRACKNETV3 predict.py
# ─────────────────────────────────────────────────────────────────────────────
def run_tracknet(
    video_path: str,
    weights_dir: str,
    raw_out_dir: str,
    eval_mode: str  = "weight",
    batch_size: int = 4,
    large_video: bool = False,
) -> Path:
    """
    Call TrackNetV3 predict.py with the CORRECT argument names and return
    the path to the auto-generated CSV file.

    TrackNetV3 argument names (verified from predict.py source)
    ────────────────────────────────────────────────────────────
      --video_file          ← NOT --video
      --tracknet_file       ← NOT --tracknet
      --inpaintnet_file     ← NOT --inpaintnet
      --save_dir            ← output folder; CSV auto-named {stem}_ball.csv
      --eval_mode           nonoverlap | average | weight
      --batch_size
      --large_video         store_true flag

    Returns
    ───────
    Path to the CSV file:  {raw_out_dir}/{video_stem}_ball.csv
    """
    wd  = Path(weights_dir).resolve()
    _validate_weights(wd)

    raw_dir = Path(raw_out_dir).resolve()   # MUST be absolute — TrackNet joins
    raw_dir.mkdir(parents=True, exist_ok=True)  # save_dir + video_stem on Windows

    # Use only the filename stem (no directory component) as the video identifier.
    # On Windows, video_path.split('/')[-1] returns the whole relative path if
    # the path uses backslashes — Path().stem handles both separators correctly.
    video_stem   = Path(video_path).stem
    expected_csv = raw_dir / f"{video_stem}_ball.csv"

    cmd = [
        sys.executable, str(wd / "predict.py"),
        "--video_file",     str(Path(video_path).resolve()),  # absolute
        "--tracknet_file",  str(wd / TRACKNET_PT),
        "--inpaintnet_file",str(wd / INPAINTNET_PT),
        "--save_dir",       str(raw_dir),   # absolute — no path doubling
        "--eval_mode",      eval_mode,
        "--batch_size",     str(batch_size),
    ]
    if large_video:
        cmd.append("--large_video")

    print("[TrackNet] Command:")
    print("  " + " ".join(cmd))
    print()

    result = subprocess.run(cmd, capture_output=False, text=True)
    # capture_output=False → TrackNet's tqdm progress bar prints live

    if result.returncode != 0:
        raise RuntimeError(
            f"TrackNetV3 predict.py exited with code {result.returncode}.\n"
            "Check the output above for the error message."
        )

    import shutil
    
    # TrackNet wrongly saves it next to the input video
    rogue_csv = Path(video_path).parent / f"{video_stem}_ball.csv"

    if rogue_csv.exists():
        # Move it from test_assets/ to results/shuttle_out/
        shutil.move(str(rogue_csv), str(expected_csv))
        print(f"[TrackNet] Intercepted CSV from video folder and moved to: {expected_csv}")
    elif not expected_csv.exists():
        # Total failure fallback
        raise FileNotFoundError(
            f"TrackNetV3 finished but no CSV found in {raw_dir} OR {rogue_csv.parent}.\n"
        )

    print(f"[TrackNet] Output CSV confirmed: {expected_csv}")
    return expected_csv


# ─────────────────────────────────────────────────────────────────────────────
#  STEP 3 — PARSE CSV
# ─────────────────────────────────────────────────────────────────────────────
def parse_tracknet_csv(csv_path: Path, video_path: str) -> np.ndarray:
    """
    Parse TrackNetV3 CSV → (N_frames, 2) float64 array.

    TrackNetV3 CSV format (exact columns):
        Frame, Visibility, X, Y
        Frame      : 0-based frame index
        Visibility : 1 = shuttle detected, 0 = not detected
        X, Y       : pixel coordinates (0,0 when Visibility==0)

    The CSV contains exactly one row per frame where the shuttle was
    processed.  We rebuild a dense array indexed by frame number,
    filling undetected frames with NaN.

    Args
    ────
    csv_path   : path to {stem}_ball.csv
    video_path : used only to get total frame count for array sizing

    Returns
    ───────
    (N_total_frames, 2) float64  — NaN rows = undetected
    """
    # Get total frame count from video so the array covers every frame
    cap    = cv2.VideoCapture(str(video_path))
    n_total = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))
    cap.release()

    # Initialise all frames as undetected
    pts = np.full((n_total, 2), np.nan, dtype=np.float64)

    with open(csv_path, newline="") as f:
        reader = csv.DictReader(f)

        # Normalise header names to lowercase for robustness
        if reader.fieldnames is None:
            raise ValueError(f"CSV has no header row: {csv_path}")

        col = {h.strip().lower(): h.strip() for h in reader.fieldnames}
        # TrackNetV3 exact column names: Frame, Visibility, X, Y
        frame_col = col.get("frame",      col.get("frame_id", "Frame"))
        vis_col   = col.get("visibility", col.get("vis",      "Visibility"))
        x_col     = col.get("x",          col.get("ball_x",   "X"))
        y_col     = col.get("y",          col.get("ball_y",   "Y"))

        rows_read = 0
        for row in reader:
            try:
                frame_idx = int(row[frame_col])
                vis       = int(row[vis_col])
                x         = float(row[x_col])
                y         = float(row[y_col])
            except (KeyError, ValueError):
                continue

            rows_read += 1
            if frame_idx >= n_total:
                # CSV may be longer than video (edge case with padding)
                continue

            if vis == 1 and not (x == 0 and y == 0):
                pts[frame_idx] = [x, y]
            # vis == 0  →  stays NaN

    n_detected = int(np.sum(~np.any(np.isnan(pts), axis=1)))
    print(f"  CSV rows read   : {rows_read}")
    print(f"  Video frames    : {n_total}")
    print(f"  Detected frames : {n_detected}  ({100*n_detected/n_total:.1f}%)")

    return pts


# ─────────────────────────────────────────────────────────────────────────────
#  STEP 4 — POST-PROCESSING
# ─────────────────────────────────────────────────────────────────────────────
def smooth_detections(pts: np.ndarray,
                      sigma: float = SMOOTH_SIGMA) -> np.ndarray:
    """
    Gaussian smooth detected positions only (NaN rows untouched).

    Why this is needed
    ──────────────────
    TrackNetV3 returns integer pixel coordinates (see predict.py: cx_pred,
    cy_pred are cast to int).  Consecutive detections of the same physical
    shuttle position jitter by ±1–2 px due to this rounding.  A light
    Gaussian (σ=1.2) on the detected sequence removes this jitter without
    affecting the genuine parabolic shape of the trajectory.
    """
    out  = pts.copy()
    mask = ~np.any(np.isnan(pts), axis=1)
    if mask.sum() < 4:
        return out   # too few points to smooth meaningfully
    for col in range(2):
        out[mask, col] = gaussian_filter1d(pts[mask, col], sigma=sigma)
    return out


def interpolate_gaps(pts: np.ndarray,
                     max_gap: int = GAP_FILL_FRAMES) -> np.ndarray:
    """
    Linear interpolation over NaN gaps of length ≤ max_gap frames.

    When to use / not use
    ─────────────────────
    Short gaps (1–5 frames): typically caused by TrackNet missing the shuttle
    on a single frame due to motion blur or occlusion by the player's body.
    Linear interpolation is accurate here because the shuttle moves nearly
    linearly over such short intervals at badminton speeds.

    Long gaps (> max_gap): genuine occlusion or shuttle out of frame.
    These are left as NaN — the physics optimiser in Module 4 is designed
    to handle missing observations and will skip NaN frames.
    """
    out = pts.copy()
    N   = len(pts)
    i   = 0
    while i < N:
        if np.any(np.isnan(out[i])):
            j = i
            while j < N and np.any(np.isnan(out[j])):
                j += 1
            gap = j - i
            if 0 < gap <= max_gap and i > 0 and j < N:
                for col in range(2):
                    out[i:j, col] = np.linspace(
                        out[i - 1, col], out[j, col], gap + 2)[1:-1]
            i = j
        else:
            i += 1
    return out


def detection_stats(pts: np.ndarray) -> dict:
    """Compute detection rate and quality grade."""
    N     = len(pts)
    valid = int(np.sum(~np.any(np.isnan(pts), axis=1)))
    rate  = valid / N if N else 0.0
    return {
        "total_frames":    N,
        "detected_frames": valid,
        "detection_rate":  rate,
        "missing_frames":  N - valid,
        "quality": (
            "EXCELLENT (>80%)"  if rate > 0.80 else
            "GOOD (60–80%)"     if rate > 0.60 else
            "POOR (<60%) — Z reconstruction accuracy will be limited"
        ),
    }


# ─────────────────────────────────────────────────────────────────────────────
#  STEP 5 — DEBUG VIDEO
# ─────────────────────────────────────────────────────────────────────────────
def render_debug_video(video_path: str, shuttle_2d: np.ndarray,
                       out_path: str):
    """
    Writes a debug copy of the input video with:
      ● Green circle    = TrackNetV3 detected position (after post-processing)
      ● Amber trail     = last TRAIL_LEN projected positions
      ● White text      = (u, v) pixel coordinates
      ● Blue text       = "undetected" on frames with no detection
      ● Grey top-right  = frame counter
    """
    cap    = cv2.VideoCapture(str(video_path))
    fps    = cap.get(cv2.CAP_PROP_FPS) or 30.0
    W_vid  = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
    H_vid  = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
    writer = cv2.VideoWriter(
        str(out_path), cv2.VideoWriter_fourcc(*"mp4v"), fps, (W_vid, H_vid))

    fi = 0
    while True:
        ok, frame = cap.read()
        if not ok:
            break

        # Amber fading trail
        start = max(0, fi - TRAIL_LEN)
        for j in range(start, fi):
            if j < len(shuttle_2d) and not np.any(np.isnan(shuttle_2d[j])):
                alpha = (j - start + 1) / (TRAIL_LEN + 1)
                c = (0, int(180 * alpha), int(255 * alpha))
                cv2.circle(frame, tuple(shuttle_2d[j].astype(int)),
                           3, c, -1, cv2.LINE_AA)

        # Current frame detection
        if fi < len(shuttle_2d) and not np.any(np.isnan(shuttle_2d[fi])):
            u, v = int(shuttle_2d[fi, 0]), int(shuttle_2d[fi, 1])
            cv2.circle(frame, (u, v), 7, (0, 255, 80), 2, cv2.LINE_AA)
            cv2.putText(frame, f"({u},{v})", (u + 10, v - 8),
                        cv2.FONT_HERSHEY_SIMPLEX, 0.5, (255, 255, 255),
                        1, cv2.LINE_AA)
        else:
            cv2.putText(frame, "undetected", (10, H_vid - 14),
                        cv2.FONT_HERSHEY_SIMPLEX, 0.55, (80, 80, 255),
                        1, cv2.LINE_AA)

        # Frame counter
        cv2.putText(frame, f"f{fi}", (W_vid - 80, 24),
                    cv2.FONT_HERSHEY_SIMPLEX, 0.55, (200, 200, 200),
                    1, cv2.LINE_AA)

        writer.write(frame)
        fi += 1

    cap.release()
    writer.release()
    print(f"Debug video → {out_path}")


