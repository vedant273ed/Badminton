# ─────────────────────────────────────────────────────────────────────────────
#  STEP 4a — OPTIONAL VIDEO PREPROCESSING  (run before TrackNet for best results)
# ─────────────────────────────────────────────────────────────────────────────
def preprocess_video(
    input_path: str,
    output_path: str,
    n_bg_frames: int   = 30,
    boost_alpha: float = 2.5,
    boost_beta: float  = -40.0,
) -> str:
    """
    Background-subtract the video before feeding it to TrackNetV3.

    Why this helps
    ──────────────
    TrackNet internally uses consecutive frame triplets and subtracts a
    median background image to isolate moving objects. When static ad banners
    (Badminton4U, HSBC, Victor) are bright white on a dark background, they
    score identically to the shuttle in the heatmap — TrackNet cannot tell them
    apart because they look the same in the raw frame.

    Pre-subtracting the background before TrackNet sees the frames removes
    these static sources entirely. The shuttle — the only bright fast-moving
    object — becomes the dominant signal in the processed frame.

    Method
    ──────
    1. Compute the per-pixel median of the first n_bg_frames frames. This
       is the "background" — static elements like the court, net, banners.
    2. Subtract the background from every frame: pixel = frame - background.
    3. Apply contrast boost (alpha, beta) to amplify the residual moving
       objects (shuttle, players) against the now-dark background.
    4. Write to output_path as an mp4 for TrackNet to process.

    Parameters
    ──────────
    input_path    original video
    output_path   where to write the processed video (fed to TrackNet)
    n_bg_frames   number of frames to use for background estimation (default 30)
    boost_alpha   contrast multiplier after subtraction (default 2.5)
    boost_beta    brightness offset after subtraction (default -40)

    Returns output_path (for chaining into run_tracknet)
    """
    cap = cv2.VideoCapture(str(input_path))
    fps = float(cap.get(cv2.CAP_PROP_FPS) or 30.0)
    W   = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
    H   = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))

    # ── Step 1: collect background frames ────────────────────────────────────
    bg_frames = []
    for _ in range(n_bg_frames):
        ok, f = cap.read()
        if not ok:
            break
        bg_frames.append(f.astype(np.float32))

    if not bg_frames:
        cap.release()
        raise RuntimeError(f"Could not read any frames from {input_path}")

    # Median background — robust to players walking through early frames
    background = np.median(np.stack(bg_frames, axis=0), axis=0).astype(np.float32)
    print(f"  [preprocess] Background estimated from {len(bg_frames)} frames")

    # ── Step 2: process all frames ────────────────────────────────────────────
    cap.set(cv2.CAP_PROP_POS_FRAMES, 0)   # rewind
    writer = cv2.VideoWriter(
        str(output_path),
        cv2.VideoWriter_fourcc(*"mp4v"),
        fps, (W, H)
    )

    fi = 0
    while True:
        ok, frame = cap.read()
        if not ok:
            break
        # Subtract background, boost contrast, clip to valid range
        diff = frame.astype(np.float32) - background
        boosted = diff * boost_alpha + boost_beta
        out_frame = np.clip(boosted, 0, 255).astype(np.uint8)
        writer.write(out_frame)
        fi += 1

    cap.release()
    writer.release()
    print(f"  [preprocess] Wrote {fi} processed frames → {output_path}")
    return str(output_path)


# ─────────────────────────────────────────────────────────────────────────────
#  STEP 4b — ROBUST CLEANING  (replaces smooth + interpolate in main pipeline)
# ─────────────────────────────────────────────────────────────────────────────
def clean_detections(
    pts: np.ndarray,
    video_path: str,
    max_speed_ms: float = 120.0,
    max_gap_linear: int = 3,
    max_gap_physics: int = 8,
    smooth_sigma: float = 1.2,
) -> np.ndarray:
    """
    Physics-based post-processing pipeline.
    Removed the brittle 'banner_frac' and replaced it with a 'Static Lock' filter.
    """
    cap   = cv2.VideoCapture(str(video_path))
    fps   = float(cap.get(cv2.CAP_PROP_FPS) or 30.0)
    img_w = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
    img_h = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))

    frames_gray = []
    while True:
        ok, f = cap.read()
        if not ok: break
        frames_gray.append(cv2.cvtColor(f, cv2.COLOR_BGR2GRAY))
    cap.release()

    N   = len(pts)
    out = pts.copy()

    px_per_m         = img_w * 0.55 / 6.7
    max_px_per_frame = max_speed_ms / fps * px_per_m * 1.4

    removed_static = removed_speed = removed_motion = 0
    filled_linear  = filled_physics = 0

    # ── A: Static Lock ("Hover") Filter ───────────────────────────────────────
    # A shuttle never hovers. If points stay within a 4-pixel radius 
    # for 3+ consecutive detections, it's a background logo.
    STATIC_FRAMES = 3
    STATIC_RADIUS = 4.0

    for i in range(N - STATIC_FRAMES + 1):
        window = out[i : i + STATIC_FRAMES]
        # Check if the whole window contains valid detections
        if not np.any(np.isnan(window)):
            # Calculate the max distance between any two points in this window
            max_dist = 0
            for p1 in window:
                for p2 in window:
                    dist = np.linalg.norm(p1 - p2)
                    if dist > max_dist:
                        max_dist = dist
            
            # If the points barely moved, it's a static background lock.
            if max_dist < STATIC_RADIUS:
                out[i : i + STATIC_FRAMES] = np.nan
                removed_static += STATIC_FRAMES

    # ── B: Speed Filter (Impossible jumps) ────────────────────────────────────
    prev_i = None
    for i in range(N):
        if np.any(np.isnan(out[i])):
            prev_i = None
            continue
        if prev_i is not None:
            dist  = float(np.linalg.norm(out[i] - out[prev_i]))
            limit = max_px_per_frame * (i - prev_i)
            if dist > limit:
                out[i] = np.nan
                removed_speed += 1
                continue
        prev_i = i

    # ── C: Motion Gate (Tightened) ────────────────────────────────────────────
    # Shrunk search radius from 32 to 8. This prevents players running nearby 
    # from triggering a false positive motion validation.
    SEARCH_R   = 8     
    MOT_THRESH = 12.0  
 
    for i in range(N):
        if np.any(np.isnan(out[i])):
            continue
        pf, nf = i - 1, i + 1
        if pf < 0 or nf >= len(frames_gray):
            continue 
 
        u, v = int(out[i, 0]), int(out[i, 1])
        x1 = max(0, u - SEARCH_R);  x2 = min(img_w, u + SEARCH_R)
        y1 = max(0, v - SEARCH_R);  y2 = min(img_h, v + SEARCH_R)
 
        pp = frames_gray[pf][y1:y2, x1:x2].astype(np.float32)
        pn = frames_gray[nf][y1:y2, x1:x2].astype(np.float32)
 
        if float(np.max(np.abs(pn - pp))) < MOT_THRESH:
            out[i] = np.nan
            removed_motion += 1

    # ── D: Gap Fill ───────────────────────────────────────────────────────────
    def _physics_fill(p0, p1, gap, fps_):
        t_total = (gap + 1) / fps_
        ts      = np.arange(1, gap + 1) / fps_
        vx = (p1[0] - p0[0]) / t_total
        g_px = 9.81 * px_per_m
        vy = (p1[1] - p0[1] - 0.5 * g_px * t_total**2) / t_total
        filled_x = p0[0] + vx * ts
        filled_y = p0[1] + vy * ts + 0.5 * g_px * ts**2
        return np.stack([filled_x, filled_y], axis=1)

    i = 0
    while i < N:
        if not np.any(np.isnan(out[i])):
            i += 1
            continue

        j = i
        while j < N and np.any(np.isnan(out[j])):
            j += 1
        gap = j - i

        if i > 0 and j < N:
            p0 = out[i - 1]
            p1 = out[j]

            if gap <= max_gap_linear:
                for col in range(2):
                    out[i:j, col] = np.linspace(p0[col], p1[col], gap + 2)[1:-1]
                filled_linear += gap

            elif gap <= max_gap_physics:
                try:
                    arc = _physics_fill(p0, p1, gap, fps)
                    arc[:, 0] = np.clip(arc[:, 0], 0, img_w - 1)
                    arc[:, 1] = np.clip(arc[:, 1], 0, img_h - 1)
                    out[i:j] = arc
                    filled_physics += gap
                except Exception:
                    for col in range(2):
                        out[i:j, col] = np.linspace(p0[col], p1[col], gap + 2)[1:-1]
                    filled_linear += gap
        i = j

    # ── E: Gaussian Smooth ────────────────────────────────────────────────────
    mask = ~np.any(np.isnan(out), axis=1)
    if mask.sum() >= 4:
        for col in range(2):
            out[mask, col] = gaussian_filter1d(out[mask, col], sigma=smooth_sigma)

    print(f"  clean_detections: "
          f"−{removed_static} static/hover  −{removed_speed} speed jumps  "
          f"−{removed_motion} static pixel  "
          f"+{filled_linear} linear  +{filled_physics} physics")
    return out


