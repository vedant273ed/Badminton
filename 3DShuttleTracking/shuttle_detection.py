"""
module2_shuttle_detection.py
════════════════════════════
Shuttlecock 2-D pixel extraction  →  (u, v) per frame

This module is written against the ACTUAL TrackNetV3 predict.py interface
(qaz812345/TrackNetV3).  Every argument name, file name, and CSV column
matches the real script exactly — verified from predict.py source.

TrackNetV3 predict.py argument mapping
───────────────────────────────────────
  --video_file        path to the video
  --tracknet_file     path to TrackNet_best.pt
  --inpaintnet_file   path to InpaintNet_best.pt   (optional)
  --save_dir          directory for outputs  (NOT --output_csv)
  --eval_mode         nonoverlap | average | weight  (default: weight)
  --batch_size        (default: 16)
  --large_video       flag for large videos
  --output_video      flag to also write annotated mp4

Output CSV name (auto-generated by TrackNetV3):
  {save_dir}/{video_stem}_ball.csv

CSV columns (TrackNetV3 format):
  Frame, Visibility, X, Y
  Visibility: 1 = detected, 0 = not detected

Pipeline
────────
  1. Validates weights folder and both .pt files exist.
  2. Runs predict.py as a subprocess with correct argument names.
  3. Locates the auto-named CSV ({video_stem}_ball.csv) in save_dir.
  4. Parses CSV → (N_frames, 2) float64; NaN where Visibility == 0.
  5. Aligns array length to actual video frame count (handles edge cases).
  6. Gaussian smoothing on detected positions only.
  7. Linear interpolation over short gaps (≤ GAP_FILL_FRAMES).
  8. Saves shuttle_2d.npy + debug video.

Usage
─────
  python module2_shuttle_detection.py
  python module2_shuttle_detection.py \\
      --video    clip.mp4 \\
      --tracknet ./tracknet_weights \\
      --out_dir  shuttle_out

  # Pass extra TrackNetV3 flags through --tn_args:
  python module2_shuttle_detection.py \\
      --video  clip.mp4 \\
      --tracknet ./tracknet_weights \\
      --tn_eval_mode nonoverlap \\
      --tn_batch_size 8 \\
      --tn_large_video

Outputs
───────
  shuttle_out/shuttle_2d.npy          (N_frames, 2) float64; NaN = undetected
  shuttle_out/shuttle_detection.mp4   debug video: green dot + amber trail
  shuttle_out/detection_stats.txt     detection rate + quality grade
  shuttle_out/tracknet_raw/           raw TrackNetV3 outputs (CSV + optional mp4)
"""

